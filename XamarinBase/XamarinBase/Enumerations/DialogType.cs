namespace XamarinBase.Enumerations
{
    /// <summary>
    /// The type of Dialog Box to show.
    /// Message: General Dialog with only one button that shows a message.
    /// Error: Dialog Box to show an error message.
    /// Confirmation: Dialog Box that allows the user too confirm or cancel.
    /// </summary>
    public enum DialogType
    {
        /// <summary>
        /// General Dialog with only one button that shows a message.
        /// </summary>
        Message,
        /// <summary>
        /// Dialog Box to show an error message.
        /// </summary>
        Error,
        /// <summary>
        /// Dialog box that allows the user too confirm or cancel.
        /// </summary>
        Confirmation
    }
}